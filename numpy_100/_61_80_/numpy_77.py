import numpy as np

Z = np.random.randint(0, 2, 100)
print(Z)
print("---------")
np.logical_not(Z, out=Z)
print(Z)

print("---------")
Z = np.random.uniform(-1.0, 1.0, 100)
print(Z)
print("---------")
np.negative(Z, out=Z)
print(Z)

