# 第九十一题：如何从常规数组创建记录数组

import numpy as np

Z = np.array([("Hello", 2.5, 3),
              ("World", 3.6, 2)])
R = np.core.records.fromarrays(Z.T,
                               names='col1, col2, col3',
                               formats='S8, f8, i8')
print(R)

# 输出内容：
# [(b'Hello', 2.5, 3) (b'World', 3.6, 2)]
