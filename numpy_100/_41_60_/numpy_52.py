# 第五十二题：对于 (100, 2) 的二维随机矢量所表示的坐标，逐点查找距离

import numpy as np
import scipy.spatial

Z = np.random.random((10, 2))
X, Y = np.atleast_2d(Z[:, 0], Z[:, 1])
D = np.sqrt((X - X.T) ** 2 + (Y - Y.T) ** 2)
print(D)

Z = np.random.random((10, 2))
D = scipy.spatial.distance.cdist(Z, Z)
print(D)

