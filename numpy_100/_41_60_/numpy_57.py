# 第五十七题：如何在二维数组中随机放置 p 个元素
# Author: Divakar

import numpy as np

n = 10
p = 3
Z = np.zeros((n, n))
X = np.random.choice(range(n*n), p, replace=False)
print(X)
np.put(Z, X, 1)
print(Z)

